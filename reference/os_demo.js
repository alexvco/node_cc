const os = require('os');

// Platform
console.log('os.platform');
console.log(os.platform());
console.log('');

// CPU Arch
console.log('os.arch');
console.log(os.arch());
console.log('');

// CPU Core Info
console.log('os.cpus');
console.log(os.cpus());
console.log('');

// Free memory
console.log('os.freemem');
console.log(os.freemem());
console.log('');

// Total memory
console.log('os.totalmem');
console.log(os.totalmem());
console.log('');

// Home dir
console.log('os.homedir');
console.log(os.homedir());
console.log('');

// Uptime
console.log('os.uptime');
console.log(os.uptime());
console.log('');
